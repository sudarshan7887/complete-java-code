
public class Switch_case 
{
	public static void main(String args[])
	{
		int i=2;
		switch(i)
		{
			case 0:
				   System.out.println("zero");
				   break;
			case 1:
				   System.out.println("one");
				   break;
			case 5:
				   System.out.println("five");
				   break;
				   
			case 7:
				   System.out.println("seven");
				   break;
			default:
				   System.out.println("Some Number");
				
			
		}
	}
}
